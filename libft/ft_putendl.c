#include "libft.h"

void	ft_putendl(char const *s)
{
	int		i;
	char	nl;

	nl = '\n';
	i = 0;
	if (s != NULL)
	{
		while (s[i] != '\0')
		{
			write(1, &s[i++], 1);
		}
		write(1, &nl, 1);
	}
}
