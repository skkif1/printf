#include "libft.h"

void	ft_strclr(char *s)
{
	size_t len;
	size_t i;

	i = 0;
	if (s != NULL)
	{
		len = ft_strlen(s);
		while (i < len)
		{
			s[i] = '\0';
			i++;
		}
	}
}
