#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	char *ptr;

	ptr = dest;
	while (*src != '\0')
	{
		*ptr++ = *src++;
	}
	*ptr++ = '\0';
	return (dest);
}
